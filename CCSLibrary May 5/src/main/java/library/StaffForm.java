    void populateTable2() {
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0); // Clear existing rows

        for (Book book : books) {
            model.addRow(new Object[]{book.getTitle(), book.getAuthor(), book.getName(), book.getBorrowDate(), book.getBorrowReturn()});
        }
    }
     
    //This is for getting the new books pare
     public void accessAvailableBooks() {
        ArrayList<Book> availableBooks = dashboard.getAllAvailableBooks();
        title = jtSearchTittleAdd.getText().trim();
        category = jComboBoxAdd.getSelectedItem().toString().trim();
        author = jtSearchAuthorAdd.getText().trim();
        available = true;
        Book newBook = new Book(title, author, category, available);
        dashboard.addBookToAvailableBooks(newBook);

        DefaultTableModel modeljTable2 = (DefaultTableModel) jTable2.getModel();
        modeljTable2.addRow(new Object[]{title, author, category});

        // Clear text fields after adding a book
        jtSearchTittleAdd.setText("");
        jComboBoxAdd.setSelectedIndex(0);
        jtSearchAuthorAdd.setText("");
    }
   


public void newBookStaffForm() {
        title = jtSearchTittleAdd.getText().trim();
        category = jComboBoxAdd.getSelectedItem().toString().trim();
        author = jtSearchAuthorAdd.getText().trim();
        available = true;

    // Add the new book to the Dashboard
    dashboard.addBook(new Book(title, author, category));

    // Add the new book to jTable2 in StaffForm
    modeljTable2 = (DefaultTableModel) jTable2.getModel();
    modeljTable2.addRow(new Object[]{title, author, category});

    // Clear text fields after adding a book
    jtSearchTittleAdd.setText("");
    jComboBoxAdd.setSelectedIndex(0);
    jtSearchAuthorAdd.setText("");

    // Print all books including the newly added book
    System.out.println("All Books:STAFF");
    for (Book book : dashboard.books) {
        System.out.println(book.getTitle() + " by " + book.getAuthor());
    }
    System.out.println("______________________________________________________");
    
}
